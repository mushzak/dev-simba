<div id="tip-u-s-stocks" class="tip-content hidden">
    <h5>Why U.S. Stocks?</h5>
    <p>U.S. Stocks are an ownership share in U.S.-based corporations. U.S. Stocks act as the core asset class because
    history demonstrates that they offer significant returns over the long run. While more volatile than bonds,
    U.S. Stocks offer a great risk-adjusted return.</p>
</div>

<div id="tip-foreign-stocks" class="tip-content hidden">
    <h5>Why Foreign Stocks?</h5>
    <p>Foreign Stocks are an ownership share in foreign companies in developed economies like Europe, Australia and Japan.
    Foreign stocks differ from U.S. stocks in two important ways. First, because markets outside the U.S. react
    differently to economic situations, foreign stocks help reduce a portfolio's overall risk. Second, foreign
    stocks provide indirect exposure to currency fluctuations. The historical returns of foreign stocks are lower than
    U.S. stocks and have higher risk, so the main impetus for investing in foreign stocks are their diversifying qualities.</p>
</div>

<div id="tip-emerging-markets" class="tip-content hidden">
    <h5>Why Emerging Markets?</h5>
    <p>Emerging Markets Stocks are an ownership share in foreign companies in newly developing economies like Brazil,
    Russia, India and China. Emerging Markets Stocks offer additional diversification because these investments
    respond differently than foreign developed economies. The historical returns of Emerging Markets Stocks are
    higher than U.S. Stocks, with higher risk. Emerging Markets add the possibility of higher returns to a portfolio
    and additional diversification.</p>
</div>

<div id="tip-dividend-stocks" class="tip-content hidden">
    <h5>Why Dividend Growth Stocks?</h5>
    <p>Dividend Growth Stocks are U.S. stocks that have a history of increasing their dividend payouts over time. They
    tend to be large-cap well-run companies in less cyclical industries and thus less volatile. As of this writing,
    many companies in this asset class have higher dividend yields than their corporate bond yields and the yields
    on U.S. government bonds. In the current low interest rate environment, Dividend Growth Stocks emerge as an
    asset class that offers an income stream and capital growth potential.</p>
</div>

<div id="tip-real-estate" class="tip-content hidden">
    <h5>Why Real Estate?</h5>
    <p>Real Estate represents investments in real estate companies that provide exposure to commercial properties,
    apartment complexes and retail space. Real Estate offers both recurring cash that is distributed to shareholders
    and an investment in the long-term value of the properties. Real Estate also acts as a hedge against inflation.
    Significantly different from both stocks and bonds, real estate is a powerful diversifier.</p>
</div>

<div id="tip-tips" class="tip-content hidden">
    <h5 >Why TIPS?</h5>
    <p>Treasury Inflation-Protected Securities (TIPS) are inflation-indexed bonds issued by the U.S. federal government.
    Unlike nominal bonds, TIPS' principal and coupons are adjusted periodically based on the Consumer Price Index (CPI).
    Although TIPS currently have historical low yields, their inflation-indexed feature and low volatility makes
    them the only asset class that can provide income generation and inflation protection to risk averse investors.</p>
</div>

<div id="tip-corporate-bonds" class="tip-content hidden">
    <h5>Why Corporate Bonds?</h5>
    <p>Corporate Bonds are debt issued by U.S. corporations with investment-grade credit ratings to fund business
    activities. They offer higher yields than U.S. Government Bonds due to higher credit risk, illiquidity and
    callability. In contrast to the U.S. government, most companies have gone through a deleveraging process and
    strengthened their balance sheets.</p>
</div>

<div id="tip-emerging-market-bonds" class="tip-content hidden">
    <h5>Why Emerging Market Bonds?</h5>
    <p>Emerging Market Bonds are debt issued by governments and quasi-government organizations from emerging market
    countries. They offer higher yields than developed market bonds. Historically, Emerging Market Bonds had serial
    defaults in the 1980s, 1990s and even 2000s. However, the world has changed, where investors today worry about
    potential defaults from developed market bonds rather than emerging market bonds. Emerging market countries,
    with younger demographics, stronger economic growth, healthier balance sheets and lower debt-to-GDP ratios,
    have less risk than most investors realize with respect to borrowing money.</p>
</div>

