<div id="tip-rebalancing-faq" class="tip-content hidden">
    <p>Your portfolio allocation will drift over time, as some of your holdings outperform others. We continuously
    monitor your portfolio and periodically rebalance it back to your target mix to manage your risk exposure and
    produce more stable returns. When rebalancing, we carefully consider asset class volatility and tax implications.
    Whenever possible we use the reinvestment of your dividends and any subsequent deposits as an opportunity to
    bring your portfolio back to your target mix with purchases rather than sales.</p>
    <p>Analyses by David Swensen,
    the Chief Investment Officer of Yale University, and Burton Malkiel, Emeritus Professor at Princeton University
    and Simba Capital CIO, with Charles Ellis, Founder of Greenwich Associates, found that over two separate 10 year
    periods <b>rebalanced portfolios earned an average of 0.4% more per year</b> versus portfolios that were not rebalanced.*</p>
    <p>Rebalancing will not always increase returns over all periods, but it will always reduce your portfolio risk
    over time and ensure that your portfolio remains consistent with your target allocation.</p>
    <p>*Sources: David Swensen, <i>Unconventional Success</i> (2005) 195 & Burton Malkiel & Charles Ellis,
    <i>The Elements of Investing</i> (2013) 69.</p>
</div>

